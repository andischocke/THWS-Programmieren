package exercise14.procedural;

public class Rectangle
{
    private double sideX;
    private double sideY;

    public Rectangle(double sideX, double sideY)
    {
        this.sideX = sideX;
        this.sideY = sideY;
    }

    public double getSideX()
    {
        return sideX;
    }

    public double getSideY()
    {
        return sideY;
    }
}
