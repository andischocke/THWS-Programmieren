package exercise15.task3;

public class PersonAddress
{
    public int zipCode;
    public String city;
    public String street;
    public String houseNumber;

    public PersonAddress(int zipCode, String city, String street, String houseNumber)
    {
        this.zipCode = zipCode;
        this.city = city;
        this.street = street;
        this.houseNumber = houseNumber;
    }


}
